/* Switch Case - Controlador de Fluxo */

const temperature = 30

switch (temperature) {
    case 10:
        console.log("Tá meio frio")
        break

    case 20:
        console.log("Temperatura ambiente")
        break

    case 30:
        console.log("Tá meio quente")
        break

    case 40:
        console.log("Tá QUENTE")
        break

    case 50:
        console.log("Alerta de ensolação")
        break

    default:
        console.log ("Não sei")
        break 
}